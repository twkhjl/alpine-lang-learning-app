create or replace function public.admin_delete_tag(
	p_tag_id integer
)
returns boolean
language plpgsql
as $$
declare
	tag_usage_count integer;
begin
	if not exists (select 1 from public.tags where id = p_tag_id) then
		return false;
	end if;

	select count(*)
	into tag_usage_count
	from public.word_tags
	where tag_id = p_tag_id;

	if tag_usage_count > 0 then
		raise exception 'Tag is still in use.';
	end if;

	delete from public.tags where id = p_tag_id;
	return true;
end;
$$;

