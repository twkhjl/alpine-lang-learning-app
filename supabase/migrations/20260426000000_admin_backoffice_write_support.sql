create or replace function public.validate_word_save_payload(
	p_translations jsonb,
	p_tag_ids integer[]
)
returns void
language plpgsql
as $$
declare
	normalized_tag_ids integer[];
	distinct_tag_count integer;
	existing_tag_count integer;
begin
	if coalesce(btrim(coalesce(p_translations->'zh-TW'->>'text', '')), '') = ''
		and coalesce(btrim(coalesce(p_translations->'id'->>'text', '')), '') = ''
		and coalesce(btrim(coalesce(p_translations->'en'->>'text', '')), '') = '' then
		raise exception 'At least one translation is required.';
	end if;

	normalized_tag_ids := coalesce(p_tag_ids, '{}'::integer[]);

	if exists (
		select 1
		from unnest(normalized_tag_ids) as tag_id
		where tag_id is null or tag_id <= 0
	) then
		raise exception 'Tag ids must be positive integers.';
	end if;

	select count(distinct tag_id), count(*)
	into distinct_tag_count, existing_tag_count
	from unnest(normalized_tag_ids) as tag_id;

	if distinct_tag_count <> existing_tag_count then
		raise exception 'Duplicate tag ids are not allowed.';
	end if;

	select count(*)
	into existing_tag_count
	from public.tags
	where id = any(normalized_tag_ids);

	if existing_tag_count <> cardinality(normalized_tag_ids) then
		raise exception 'One or more tags do not exist.';
	end if;
end;
$$;

create or replace function public.replace_word_relations(
	p_word_id integer,
	p_translations jsonb,
	p_tag_ids integer[]
)
returns void
language plpgsql
as $$
begin
	perform public.validate_word_save_payload(p_translations, p_tag_ids);

	insert into public.word_translations (word_id, language_code, text, pronunciation, audio_filename)
	values
		(
			p_word_id,
			'zh-TW',
			coalesce(btrim(coalesce(p_translations->'zh-TW'->>'text', '')), ''),
			coalesce(btrim(coalesce(p_translations->'zh-TW'->>'pronunciation', '')), ''),
			coalesce(btrim(coalesce(p_translations->'zh-TW'->>'audio_filename', '')), '')
		),
		(
			p_word_id,
			'id',
			coalesce(btrim(coalesce(p_translations->'id'->>'text', '')), ''),
			coalesce(btrim(coalesce(p_translations->'id'->>'pronunciation', '')), ''),
			coalesce(btrim(coalesce(p_translations->'id'->>'audio_filename', '')), '')
		),
		(
			p_word_id,
			'en',
			coalesce(btrim(coalesce(p_translations->'en'->>'text', '')), ''),
			coalesce(btrim(coalesce(p_translations->'en'->>'pronunciation', '')), ''),
			coalesce(btrim(coalesce(p_translations->'en'->>'audio_filename', '')), '')
		)
	on conflict (word_id, language_code) do update
	set
		text = excluded.text,
		pronunciation = excluded.pronunciation,
		audio_filename = excluded.audio_filename;

	delete from public.word_tags
	where word_id = p_word_id;

	insert into public.word_tags (word_id, tag_id)
	select p_word_id, tag_id
	from unnest(coalesce(p_tag_ids, '{}'::integer[])) as tag_id;
end;
$$;

create or replace function public.admin_create_word(
	p_image_url text default '',
	p_translations jsonb default '{}'::jsonb,
	p_tag_ids integer[] default '{}'::integer[]
)
returns public.words
language plpgsql
as $$
declare
	saved_word public.words;
begin
	insert into public.words (image_url)
	values (coalesce(btrim(coalesce(p_image_url, '')), ''))
	returning * into saved_word;

	perform public.replace_word_relations(saved_word.id, p_translations, p_tag_ids);

	select *
	into saved_word
	from public.words
	where id = saved_word.id;

	return saved_word;
end;
$$;

