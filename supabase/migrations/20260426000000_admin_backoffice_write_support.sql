create or replace function public.touch_word_updated_at()
returns trigger
language plpgsql
as $$
declare
	target_word_id integer;
begin
	target_word_id := coalesce(new.word_id, old.word_id);

	if target_word_id is not null then
		update public.words
		set updated_at = now()
		where id = target_word_id;
	end if;

	return coalesce(new, old);
end;
$$;

create or replace function public.set_word_updated_at()
returns trigger
language plpgsql
as $$
begin
	new.updated_at = now();
	return new;
end;
$$;

