	where id = p_tag_id;

	insert into public.tag_translations (tag_id, language_code, name)
	values
		(
			p_tag_id,
			'zh-TW',
			coalesce(btrim(coalesce(p_translations->'zh-TW'->>'name', '')), '')
		),
		(
			p_tag_id,
			'id',
			coalesce(btrim(coalesce(p_translations->'id'->>'name', '')), '')
		),
		(
			p_tag_id,
			'en',
			coalesce(btrim(coalesce(p_translations->'en'->>'name', '')), '')
		)
	on conflict (tag_id, language_code) do update
	set name = excluded.name;
end;
$$;

